Write-Log " SEED SUMMARY" INFO
Write-Log "────────────────────────────────────────────" INFO
Write-Log "  Unique tags found         : $($uniqueTags.Count)"   INFO
Write-Log "  Dummy VMs created         : $($Stats.VMsCreated)"   INFO
Write-Log "  Dummy VMs found in fabric : $($Stats.VMsFound)"     INFO
Write-Log "  Dummy VMs tagged          : $($Stats.VMsTagged)"    INFO
Write-Log "  Tag errors                : $($Stats.TagErrors)"    $(if ($Stats.TagErrors -gt 0) { 'ERROR' } else { 'INFO' })
Write-Log "  Skipped                   : $($Stats.Skipped)"      WARN
Write-Log "  Errors                    : $($Stats.Errors)"       $(if ($Stats.Errors -gt 0) { 'ERROR' } else { 'INFO' })
Write-Log "────────────────────────────────────────────" INFO
Write-Log " NEXT STEPS" INFO
Write-Log "  1. Run Import-NSX-DFW.ps1 to import DFW objects (groups, policies)." INFO
Write-Log "     Tag-based groups will already resolve against the dummy VMs." INFO
Write-Log "  2. Migrate your real VMs to the destination environment." INFO
Write-Log "  3. Re-apply tags to real VMs using Import-NSX-DFW.ps1 -ImportTags `$true." INFO
Write-Log "  4. Delete the '$DummyVMPrefix*' dummy VMs from vCenter." INFO
Write-Log "════════════════════════════════════════════" INFO